class NameSwitch

	{
		
		public static void main(String args[])
		{
			String name="Justin";
			switch (name)
			{
				case "Justin" :
				{
					System.out.println(name);
					break;
				}
				case "Alwin" :
				{

					System.out.println(name);
					break;
				}

				case "Mohan" :
				{

					System.out.println(name);
					break;
				}
			}
		}
	}