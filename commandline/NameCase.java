class NameCase

	{
		public static void main(String args[])
		{
			String name="Justin";
			switch (name)
			{
				case "Justin":
				{
					System.out.println(name);
					break;
				}
				case "alwin":
				{

					System.out.println(name);
					break;
				}

				case "JuStin":
				{

					System.out.println(name);
					break;
				}
				default:
					System.out.println("None of them are matching");
			}
		}
	}